package Composition;

public class Resolution {

	public int getHeight() {
		return height;
	}
	public int getWeidth() {
		return weidth;
	}
	public Resolution(int height, int weidth) {
		super();
		this.height = height;
		this.weidth = weidth;
	}
	private int height;
	private int weidth;
}
